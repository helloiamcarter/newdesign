        <h1>Head Lice</h1>
        <p>Head lice are small parasitic insects that thrive in human hair by feeding on tiny amounts of blood from the scalp. Head lice do not transmit any diseases, but they are very contagious and can be very itchy.</p>
        <p>They are characterized by the combination of small red bumps and tiny white specks (also known as eggs or nits) on the bottom of hair closest to the skin (less than a quarter-inch from the scalp).</p>
        <p>Head lice are visible to the naked eye. The eggs look like yellow, tan or brown dots on a hair. Live lice can also be seen crawling on the scalp. They can also survive up to two days off of the scalp.</p>
        <p>Head lice are spread through head-to-head contact; by sharing clothing, linens, combs, brushes, hats and other personal products; or by lying on upholstered furniture or beds of an infested person.</p>
        <p>You can determine if your child has head lice by parting the child's hair and looking for nits or lice, particularly around the ears and nape of the neck. If one member of your family is diagnosed with head lice, you'll need to check on every member of the same household.</p>
        <p>Medicated lice treatments include shampoos, cream rinses and lotions that kill the lice. Many of these are over-the-counter, but prescription drugs are available for more severe cases. It is important to use these medications exactly as instructed and for the full course of treatment to eliminate the lice.</p>
        <p>Do not use a cream rinse, conditioner or combined shampoo and conditioner on your hair before a lice treatment. You also should not shampoo for one or two days following the application of a treatment.
        <p>After applying the medicated treatment, use a special comb to comb out any nits on the scalp. Repeat the entire treatment seven to ten days after the initial treatment to take care of any newly hatched lice. Please note that you should not treat a person more than three times with any individual lice medication.</p>

        <p>To get rid of the lice, you’ll also have to wash all bed linens and clothing warm by the infested person in very hot water, dry clean clothing that is not machine washable, vacuum upholstery in your home and car, soak combs, brushes, headbands and other hair accessories in rubbing alcohol or medicated shampoo for at least one hour or throw them away.</p>
