        <p>There are literally hundreds of different kinds of lumps, bumps and cysts associated with the skin. Fortunately, the vast majority of these are harmless and painless.</p>
        <p>Dermatofibromas are a red, brown or purple growth and are generally benign. They are usually found on arms and legs and feels like a hard lump.</p>
        <p>They can be  itchy, tender to the touch, and sometimes painful. Dermatofibromas usually do not require treatment. The most common removal method is surgical excision or cryotherapy (freezing it off with liquid nitrogen)</p>

        <p>Epidermoid Cysts (Sebaceous Cysts) are round small bumps, usually white or yellow, that most commonly appear on the face, back, neck, trunk and genitals. They are usually benign but occasionally lead to basal or squamous cell skin cancers.</p>
        <p>If infected, epidermoid cysts will become red and tender. They also can produce a thick yellow, cheese-like discharge when squeezed. Treatments include antibiotics if there is an underlying infection, and laser surgery may be used for sensitive areas of the skin, like the face</p>

        <p>Folliculitis are red pimples around areas having hair. Treatments usually include topical antibiotics, oral antibiotics, and antifungal medications.</p>

        <p>Keratoacanthoma are red, dome-shaped, thick bumps with craters in the center. They are triggered by minor skin injury such as a cut or bug bite, ultraviolet radiation from sun exposure is the most common risk factor. Treatments involve cryotherapy and curettage (surgically cutting out or scraping off).</p>

        <p>Keratosis Pilaris are small, rough white or red bumps that neither itch nor hurt. They are usually worse during winter months or when there is low humidity and the skin gets dry. Keratosis Pilaris usually does not require treatment and in most cases disappears on its own by age 30.</p>
        <p>Intensive moisturizing is the first line of treatment, but for more difficult cases medicated creams with urea or alpha-hydroxy acids can be used.</p>

